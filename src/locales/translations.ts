// Type helper for translation keys
type NestedKeyOf<T, K extends string = ""> = T extends object
  ? {
      [P in keyof T & string]: T[P] extends object
        ? NestedKeyOf<T[P], K extends "" ? P : `${K}.${P}`>
        : K extends ""
        ? P
        : `${K}.${P}`;
    }[keyof T & string]
  : never;

export type TranslationKey = NestedKeyOf<typeof translations.en>;
